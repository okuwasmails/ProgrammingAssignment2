cacheSolve <- function(x, ...) {
        ## Return a matrix that is the inverse of 'x'
        inv <- x$getInv()
        if(!is.null(inv)) {
                return(inv)
        }
        d <- x$get()
        inv <- solve(d)
        x$setInv(inv)
        inv
}
